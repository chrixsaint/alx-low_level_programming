#include <stdio.h>

/**
 * add - this does what you see.
 * @a: Operand 1.
 * @b: operand 2.
 * Return: returns result.
 */
int add(int a, int b)
{
	return a + b;
}
/**
 * sub - this does what you see.
 * @a: Operand 1.
 * @b: operand 2.
 * Return: returns result.
 */
int sub(int a, int b)
{
	return a - b;
}
/**
 * mul - this does what you see.
 * @a: Operand 1.
 * @b: operand 2.
 * Return: returns result.
 */
int mul(int a, int b)
{
	return a * b;
}
/**
 *div - this does what you see.
 * @a: Operand 1.
 * @b: operand 2.
 * Return: returns result.
 */
int div(int a, int b)
{
	return a / b;
}
/**
 * mod - this does what you see.
 * @a: Operand 1.
 * @b: operand 2.
 * Return: returns result.
 */
int mod(int a, int b)
{
	return 	a % b;
}